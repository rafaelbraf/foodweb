    private void buttonSalvarCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarCategoriaActionPerformed
        CategoriaService categoriaService = new CategoriaService();
        
        Restaurante restaurante = categoriaService.pegaCategoriaPorId(idCategoria).getRestaurante();
        
        String situacaoCategoria = comboBoxSituacaoCategoria.getSelectedItem().toString();
        
        Categoria categoria = new Categoria();
        categoria.setNome(tfNomeCategoria.getText());
        
        boolean categoriaAtivo = situacaoCategoria == "Ativo";
        
        categoria.setAtivo(categoriaAtivo);
        categoria.setRestaurante(restaurante);
        
        boolean categoriaAtualizada = categoriaService.atualizaCategoriaPorId(idCategoria, categoria);
        
        if (categoriaAtualizada) {
            JOptionPane.showMessageDialog(null, "Categoria editada com sucesso!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao editar categoria.");
        }
    }//GEN-LAST:event_buttonSalvarCategoriaActionPerformed


