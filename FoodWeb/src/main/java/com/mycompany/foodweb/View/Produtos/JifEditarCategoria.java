    public void carregaCategoriaParaEdicao(Long idCategoria) {
        
        try {
            
            CategoriaService categoriaService = new CategoriaService();
            Categoria categoria = categoriaService.pegaCategoriaPorId(idCategoria);
            
            tfNomeCategoria.setText(categoria.getNome());
            
            DefaultComboBoxModel comboBoxSituacao = (DefaultComboBoxModel) comboBoxSituacaoCategoria.getModel();
            comboBoxSituacao.removeAllElements();
            
            boolean categoriaAtiva = categoria.getAtivo();
            if (categoriaAtiva) {
                comboBoxSituacao.addElement("Ativo");
                comboBoxSituacao.addElement("Desativado");
            } else {
                comboBoxSituacao.addElement("Desativado");
                comboBoxSituacao.addElement("Ativo");
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar as informações da categoria.");
        }
        
    }

